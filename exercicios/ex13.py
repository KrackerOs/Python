largura = float(input('Digite a largura: '))
altura = float(input('Digite a altura: '))
area = largura*altura 
tinta = area/2
print('A largura digitada em metros foi {}, ja a quantidade de metros de altura digitada foi {}, com isso em mente, a quantidade de area é {}, necessitando de {} litros de tinta'.format(largura, altura, area, tinta))
