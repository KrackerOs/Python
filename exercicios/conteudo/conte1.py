seila = input('Digite algo: ')
print(seila.isalpha()) #Serve para letras
print(seila.isnumeric()) #Serve para numeros
print(seila.isalnum()) #Serve para letras e numeros
print(seila.isupper()) #Serve para letras maiusculas
print(seila.isdecimal()) #Para numeros com virgulas
print(seila.islower()) #Para numeros minusculos
print(seila.isspace()) #Serve para usar espaço